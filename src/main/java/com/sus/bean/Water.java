package com.sus.bean;

public class Water {
    private Integer waterStock;

    private Integer waterReservation;

    private Integer waterExtract;

    public Integer getWaterStock() {
        return waterStock;
    }

    public void setWaterStock(Integer waterStock) {
        this.waterStock = waterStock;
    }

    public Integer getWaterReservation() {
        return waterReservation;
    }

    public void setWaterReservation(Integer waterReservation) {
        this.waterReservation = waterReservation;
    }

    public Integer getWaterExtract() {
        return waterExtract;
    }

    public void setWaterExtract(Integer waterExtract) {
        this.waterExtract = waterExtract;
    }
}